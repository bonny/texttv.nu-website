	<h2>Vad är cookies</h2>
	
	<p>
		Cookies (även kallade för "kakor" på svenska) är små filer som skickas till din webbläsare och sparas av din webbläsare. 
		De används för att lagra olika typer av information.</p>
			
	<h2>Cookies på TextTV.nu</h2>

	<p>Vi använder cookies för att mäta trafiken på vår webbplats, för statistik och för att anpassa annonser.</p>
	
	<h3>Cookies som sätts och används</h3>

	<ul>
		<li>
			<p><em>Namn:</em> stats</p>
			<p><em>Syfte:</em> Används för att lagra statistik om vilka sidor du som användare besöker.</p>
		</li>

		<li>
			<p><em>Namn:</em> cookieConsent</p>
			<p><em>Syfte:</em> Används för att komma ihåg att du godkänt cookies.</p>
		</li>

		<li>
			<p><em>Namn:</em> __gads</p>
			<p><em>Syfte:</em> Används för att mäta annonsers effektivitet samt för att begränsa antalet gånger en annons visas för en användare.</p>
		</li>
		<li>
			<p><em>Namn:</em> _ga, _gid och _gat</p>
			<p><em>Syfte:</em> _ga och _git används av Google Analytics för att unikt identifiera användare.
				_gat används av Google Analytics för och för att begränsa antalet anrop till Google.</p>
			<p><a href="https://developers.google.com/analytics/devguides/collection/analyticsjs/cookie-usage">Mer information om dessa cookies</a> hos Google.
		</li>
	</ul>

