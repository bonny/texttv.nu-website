	<h1>TextTV.nu är en smartare text-tv för din dator, mobil och surfplatta</h1>
	
	<h2>Text-tv – för att 2 miljoner svenskar kan inte ha fel</h2>
	
	<p>Trots att text-tv har många år på nacken så används den fortfarande flitigt.
	<a href="http://www.svt.se/omsvt/om-sandningarna/utsandning/text-tv">Enligt SVT</a> så använder fler än 2 miljoner personer SVT Text varje dag.</p>

	<h2>Vårt mål: lite bättre Text TV</h2>
	
	<p>Vi är kanske lite galna, men vi tror att det går att göra klassiska text-tv ännu lite bättre. Några av de saker som utmärker <a href="/" title="Till Text-TV sida 100">texttv.nu</a> är:</p>
	
	<ul>
		<li>Bättre anpassad för smartphones som iPhone och diverse Android</li>
		<li>Stöd för permalänkar, så det går att dela en en nyhet på Twitter och Facebook - utan att nyheten "skrivs över" nästa dag</li>
		<li>Möjlighet att visa flera sidor samtidigt. T.ex. såhär: 
			<ul>
				<li><a href="/101-103" title="Till text-tv sida 101 till och med 103">Inrikesnyheter</a>, sid 101 till 103</li>
				<li><a href="/100,300" title="Till text-tv sida 100 (toppnyheter/löpet) och sida 300 (sportnyheter)">Startsida 100 och sportnyheterna på sidan 300</a></li>
				<li><a href="/601-604" title="Till text-tv sida 601 till och med 604 (tv)">På TV Svt 1 & Svt 2, dvs. sidorna 601-604</a></li>
			</ul>
	</ul>
	
